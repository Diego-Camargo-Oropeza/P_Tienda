    private void jt_productsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jt_productsMouseClicked

        int fila = jt_products.getSelectedRow();

        ct_id.setText(jt_products.getValueAt(fila, 0).toString());
        ct_description.setText(jt_products.getValueAt(fila, 1).toString());
        cmb_brand.setSelectedItem(jt_products.getValueAt(fila, 2).toString());
        ct_content.setText(jt_products.getValueAt(fila, 3).toString());
        ct_price.setText(jt_products.getValueAt(fila, 5).toString());

        if (jt_products.getValueAt(fila, 4).toString().equals("Abarrotes")) {
            rb_abarrotes.setSelected(true);
        } else if (jt_products.getValueAt(fila, 4).toString().equals("Higiene Personal")) {
            rb_higiene.setSelected(true);
        } else if (jt_products.getValueAt(fila, 4).toString().equals("Frutas y Verduras")) {
            rb_frutas_verduras.setSelected(true);
        } else {
            rb_vinos_licores.setSelected(true);
        }

        if (jt_products.getValueAt(fila, 6).toString().equals("true")) {
            cb_status.setSelected(true);
        } else {
            cb_status.setSelected(false);
        }

        String fecha1 = jt_products.getValueAt(fila, 7).toString();
        Date date1;
        try {
            date1 = new SimpleDateFormat("yyyy-MM-dd").parse(fecha1);
            jdc_prodDate.setDate(date1);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, "Error al convertir fecha");
        }

        if (jt_products.getValueAt(fila, 8) != null) {
            String fecha2 = jt_products.getValueAt(fila, 8).toString();
            Date date2;
            try {
                date2 = new SimpleDateFormat("yyyy-MM-dd").parse(fecha2);
                jdc_expDate.setDate(date2);
            } catch (ParseException e) {
                JOptionPane.showMessageDialog(null, "Error al convertir fecha");
            }
        } else {
            jdc_expDate.setDate(null);
        }

    }//GEN-LAST:event_jt_productsMouseClicked

