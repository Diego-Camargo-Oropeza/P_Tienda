    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        int fila = jt_products.getSelectedRow();
        int res;
        boolean isBorrado;
        if (fila != -1) {
            res = JOptionPane.showConfirmDialog(this, "¿De verdad desea eliminar el producto: '" + jt_products.getValueAt(fila, 2).toString() + "'? esto no se podrá revertir\n",
                    "Confirmación", JOptionPane.YES_NO_OPTION);
            if (res == JOptionPane.YES_OPTION) {
                isBorrado = PRODUCTO.eliminarProducto(Integer.parseInt(jt_products.getValueAt(fila, 0).toString()));
                this.construirTabla();
                if (!isBorrado) {
                    JOptionPane.showMessageDialog(this, "Algo ha pasado al intentar borrar...");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe de seleccionar un elemento de la tabla...");
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

