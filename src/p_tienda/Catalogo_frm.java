    private void agregar_actualizar(boolean agregar) {

        int id = 0, res = 0;
        String desc, marca = null, categoria = null, cont, dec;
        float precio = 0.0f;
        boolean estado, valido = true, val;

        try {
            id = Integer.parseInt(ct_id.getText());
            if (id < 1) {
                JOptionPane.showMessageDialog(this, "El ID debe de ser mayor a 0");
                valido = false;
                ct_id.requestFocus();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID debe debe de ser numérico");
            valido = false;
            ct_id.requestFocus();
        }

        desc = ct_description.getText();
        cont = ct_content.getText();

        try {
            marca = cmb_brand.getSelectedItem().toString();
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(this, "La descripcion no puede estar vacía");
            valido = false;
            cmb_brand.requestFocus();
        }
        if (desc.isEmpty() || desc.isBlank()) {
            JOptionPane.showMessageDialog(this, "La descripcion no puede estar vacía");
            valido = false;
            ct_description.requestFocus();
        }

        if (cont.isEmpty() || cont.isBlank()) {
            JOptionPane.showMessageDialog(this, "El contenido no puede estar vacío");
            valido = false;
            ct_content.requestFocus();
        }

        try {
            precio = Float.parseFloat(ct_price.getText());
            if (precio < 0) {
                JOptionPane.showMessageDialog(this, "El precio no puede ser negativo");
                valido = false;
                ct_price.requestFocus();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El precio debe de ser numérico");
            valido = false;
            ct_price.requestFocus();
        }

        if (cb_status.isSelected()) {
            estado = true;
        } else {
            estado = false;
        }

        if (rb_abarrotes.isSelected()) {
            categoria = rb_abarrotes.getText();
        } else if (rb_higiene.isSelected()) {
            categoria = rb_higiene.getText();
        } else if (rb_frutas_verduras.isSelected()) {
            categoria = rb_frutas_verduras.getText();
        } else if (rb_vinos_licores.isSelected()) {
            categoria = rb_vinos_licores.getText();
        }

        LocalDate now = LocalDate.now();
        java.sql.Date fechaE = null;
        java.sql.Date fechaC = null;
        Date fE = jdc_prodDate.getDate();

        // --- Validación de Fecha de Elaboración ---
        if (fE != null) {
            // java.util.Date a LocalDate para una comparar
            LocalDate prodDate = fE.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

            // Se verifica si la fecha de elaboración es futura
            if (prodDate.isAfter(now)) {
                valido = false;
                JOptionPane.showMessageDialog(this, "Ingrese una fecha correcta. La fecha de elaboración no puede ser futura.");
                jdc_prodDate.requestFocus();
                return;
            }

            // Si la fecha es válida, la asignamos
            long dE = fE.getTime();
            fechaE = new java.sql.Date(dE);

        } else {

            JOptionPane.showMessageDialog(this, "La fecha de elaboración es un dato requerido", "Advertencia", JOptionPane.WARNING_MESSAGE);
            valido = false;
            jdc_prodDate.requestFocus();
            return;
        }

        // --- Validación de Fecha de Caducidad ---
        Date fC = jdc_expDate.getDate();

        if (fC != null) {
            // Convertimos ambas fechas a LocalDate para comparar
            LocalDate expDate = fC.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

            LocalDate prodDate = fechaE.toLocalDate();

            //Verifica que la fecha de caducidad NO sea ANTERIOR a la de elaboración
            if (expDate.isBefore(prodDate)) {
                valido = false;
                JOptionPane.showMessageDialog(this, "La fecha de elaboracion no debe de exceder la fecha de caducidad");
                jdc_expDate.requestFocus();
                return;
            }

            long dC = fC.getTime();
            fechaC = new java.sql.Date(dC);
        }

        if (agregar && valido) {
            res = PRODUCTO.insertarProducto(id, desc, marca, cont, categoria, precio, estado, fechaE, fechaC);
        }

        if (!agregar && valido) {
            res = PRODUCTO.actualizarProducto(id, desc, marca, cont, categoria, precio, estado, fechaE, fechaC);

        }

        if (res > 0) {
            this.limpiar();
            this.construirTabla();
        }

    }

    private void construirTabla() {
        jt_products.setModel(PRODUCTO.getDatos());
    }

